class Solution{   
public:
    void leftRotate(int nums[], int n, int d)
    {
        vector<int> ans(n);
        for(int i = 0; i <n; ++i){
            ans[i] = nums[(i+d)%n];
        }

        for(int i = 0; i < n; ++i){
            nums[i] = ans[i];
        }
    }
};

